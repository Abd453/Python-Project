def move_forward(hen, child, child_2, number_of_move):
    for m in range(number_of_move):
        hen.move(-1,0)
        child.move(-1,0)
        time.sleep(0.0003)
        child_2.move(-1,0)
    for k in range(number_of_move):
        sun = sun_obj()
        sun.move(1,0)
move_forward(hen, child,child_2, 750)
