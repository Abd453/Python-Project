GROUND.setBorderWidth(2)
# SUN
def sun_obj():
    SUN = Layer()
    sun = Circle(30, Point(10,40))
    sun.setFillColor("yellow")
    sun.setBorderWidth(0)
    SUN.add(sun)
    ray = Path(Point(40, 60), Point(60, 75))
    ray.setBorderWidth(3)
    ray.setBorderColor("yellow")
    SUN.add(ray)
    ray2 = ray.clone()
    ray3 = ray.clone()
    ray2.move(5,-30)
    ray2.rotate(-60)
    ray3.move(-25, 15)
    ray3.rotate(40)
    SUN.add(ray3)
    SUN.add(ray2)
    return SUN
our_village.add(sun_obj())
our_village.add(GROUND)
